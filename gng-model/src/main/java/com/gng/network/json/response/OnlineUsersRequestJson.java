package com.gng.network.json.response;


public class OnlineUsersRequestJson {
	private Integer userId;
	private boolean getOnlineUsers;
	
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public boolean isGetOnlineUsers() {
		return getOnlineUsers;
	}
	public void setGetOnlineUsers(boolean getOnlineUsers) {
		this.getOnlineUsers = getOnlineUsers;
	}
	@Override
	public String toString() {
        return "{ \"userId\" : \"" + userId + "\", \"getOnlineUsers\" : \"" + getOnlineUsers + "\"}";
    }
	
	
}
