package com.gng.network.exceptions;

public class NullObjectException extends Exception {

	private static final long serialVersionUID = -7780355293448040214L;

	public NullObjectException() {
	}

	public NullObjectException(String message) {
		super(message);
	}

	public NullObjectException(Throwable throwable) {
		super(throwable);
	}

	public NullObjectException(String message, Throwable throwable) {
		super(message, throwable);
	}

}
